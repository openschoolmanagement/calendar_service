class Person {
  personid: number;
  email: string;
  firstname: string;
  lastname: string;

  constructor(
    personid: number,
    email: string,
    firstname: string,
    lastname: string
  ) {
    this.personid = personid;
    this.email = email;
    this.firstname = firstname;
    this.lastname = lastname;
  }
}
